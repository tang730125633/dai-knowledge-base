def generate_index_md(category, subcategory, lifecycle, source, standards):
    """生成标准索引markdown"""
    if standards:
        count = len(standards)
        status = f"✅ 已索引 {count} 条"
        rows = []
        for i, (std_id, std_name) in enumerate(standards, 1):
            link = bzfxw_link(std_id)
            rows.append(f"| {i} | {std_id} | {std_name} | [搜索]({link}) | ⚠️ 需SVIP |")
        table = "\n".join(rows)
    else:
        status = "⚠️ 该领域标准较少或为新兴领域"
        table = "| - | - | 该领域暂未发现专项标准，建议参考上级品类通用标准 | - | 📌 待补充 |"

    return f"""# {subcategory} - {lifecycle} - {source}

> 品类: {category} / {subcategory}
> 生命周期: {lifecycle}
> 标准来源: {source}
> 状态: {status}

| 序号 | 标准编号 | 标准名称 | 资源链接 | 状态 |
|------|----------|----------|----------|------|
{table}

> 🔗 资源链接为 bzfxw.com（学兔兔）搜索链接，下载 PDF 需 SVIP 会员
> 📅 索引生成时间: 2026-04-01
"""

def main():
    updated = 0
    skipped = 0

    # Walk through all bottom-level directories
    for category in ["1、发电", "2、输电", "3、变电", "4、配电", "5、用电"]:
        cat_path = os.path.join(BASE, category)
        if not os.path.isdir(cat_path):
            continue

        for subcategory in sorted(os.listdir(cat_path)):
            sub_path = os.path.join(cat_path, subcategory)
            if not os.path.isdir(sub_path) or subcategory.startswith('.'):
                continue

            key = f"{category}/{subcategory}"

            # Determine which standard set to use
            if key in STANDARDS_DB:
                std_data = STANDARDS_DB[key]
            elif subcategory in VOLTAGE_LEVELS_SUBSTATION:
                std_data = get_voltage_substation_standards(subcategory)
            elif subcategory in VOLTAGE_LEVELS_TRANSMISSION:
                std_data = get_voltage_transmission_standards(subcategory)
            elif subcategory in DISTRIBUTION_GENERIC:
                std_data = get_distribution_generic_standards(subcategory)
            elif subcategory in USAGE_GENERIC:
                std_data = get_usage_generic_standards(subcategory)
            else:
                # Fallback: use empty but marked standards
                std_data = None

            for lifecycle in ["1、勘测设计", "2、造价", "3、施工", "4、验收", "5、运维"]:
                for source in ["国家标准", "行业标准", "企业标准"]:
                    file_path = os.path.join(sub_path, lifecycle, source, "标准索引.md")

                    if not os.path.exists(os.path.dirname(file_path)):
                        continue

                    # Check if file already has real data (>500 bytes, not template)
                    if os.path.exists(file_path) and os.path.getsize(file_path) > 500:
                        with open(file_path, 'r') as f:
                            content = f.read()
                        if "待爬取" not in content and "待补充" not in content:
                            skipped += 1
                            continue

                    # Get standards for this specific cell
                    if std_data and lifecycle in std_data and source in std_data[lifecycle]:
                        standards = std_data[lifecycle][source]
                    else:
                        standards = []

                    md = generate_index_md(category, subcategory, lifecycle, source, standards)

                    with open(file_path, 'w') as f:
                        f.write(md)
                    updated += 1

    print(f"✅ 完成！更新了 {updated} 个索引文件，跳过了 {skipped} 个已有数据的文件")

if __name__ == "__main__":
    main()
