# 4. 判断生命周期
# ============================================================
def guess_lifecycle(name, std_id):
    name_lower = name.lower()
    name_combined = name + std_id

    scores = {}
    for lc, keywords in LIFECYCLE_KEYWORDS.items():
        score = 0
        for kw in keywords:
            if re.search(kw, name_combined):
                score += 1
        scores[lc] = score

    best = max(scores, key=lambda k: scores[k])
    if scores[best] > 0:
        return [best]

    # 没有明确匹配 → 可能适用多个生命周期
    # 通用技术条件/技术规范适用于设计
    if any(kw in name for kw in ['技术条件', '技术参数', '技术要求', '型式试验', '通用技术']):
        return ['1、勘测设计']
    # 规程类广泛适用
    if '规程' in name:
        return ['1、勘测设计', '5、运维']
    # 默认设计+运维
    return ['1、勘测设计', '5、运维']

# ============================================================
# 5. 匹配子品类
# ============================================================
def match_subcategories(std_id, name):
    matched = []
    name_combined = name + ' ' + std_id

    for subcat, include_kws, exclude_kws in SUBCATEGORY_RULES:
        # 检查排除词
        excluded = any(re.search(kw, name_combined) for kw in exclude_kws)
        if excluded:
            continue
        # 检查包含词
        included = any(re.search(kw, name_combined) for kw in include_kws)
        if included:
            matched.append(subcat)

    return matched

# ============================================================
# 6. 主处理逻辑
# ============================================================
def main():
    standards = load_all_standards()

    # 分类结果: { "大类/子品类/生命周期/标准来源": [标准列表] }
    classified = defaultdict(list)
    unmatched = []

    for std in standards:
        std_id = std['id']
        name = std['name']
        source = get_source(std_id)
        subcats = match_subcategories(std_id, name)

        if not subcats:
            unmatched.append(std)
            continue

        lifecycles = guess_lifecycle(name, std_id)

        for subcat in subcats:
            for lc in lifecycles:
                key = f"{subcat}/{lc}/{source}"
                classified[key].append(std)

    print(f"\n✅ 成功分类: {sum(len(v) for v in classified.values())} 条次")
    print(f"❌ 未匹配: {len(unmatched)} 条")

    # 写入索引文件
    updated = 0
    skipped = 0

    for key, stds in classified.items():
        # key 格式: "1、发电/水电/1、勘测设计/行业标准"
        parts = key.split('/')
        if len(parts) != 4:
            continue
        big_cat, subcat, lifecycle, source = parts

        file_path = os.path.join(BASE, big_cat, subcat, lifecycle, source, "标准索引.md")
        if not os.path.exists(os.path.dirname(file_path)):
            continue

        # 生成索引内容
        # 去重（同一文件夹内按标准编号去重）
        seen = set()
        unique_stds = []
        for s in stds:
            if s['id'] not in seen:
                seen.add(s['id'])
                unique_stds.append(s)

        count = len(unique_stds)
        rows = []
        for i, s in enumerate(unique_stds, 1):
            url = s['url'] if s['url'] else f"https://www.bzfxw.com/search.asp?key={s['id'].replace('/', '%2F').replace(' ', '+')}"
            rows.append(f"| {i} | {s['id']} | {s['name']} | [查看]({url}) | ⚠️ 需SVIP |")

        content = f"""# {subcat} - {lifecycle} - {source}

> 品类: {big_cat} / {subcat}
> 生命周期: {lifecycle}
> 标准来源: {source}
> 状态: ✅ 已索引 {count} 条

| 序号 | 标准编号 | 标准名称 | 资源链接 | 状态 |
|------|----------|----------|----------|------|
{chr(10).join(rows)}

> 🔗 资源链接为 bzfxw.com（学兔兔）页面链接，下载 PDF 需 SVIP 会员
> 📅 索引更新时间: 2026-04-02
"""
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(content)
        updated += 1

    print(f"\n📁 写入文件: {updated} 个")

    # 打印未匹配的标准（供调试）
    if unmatched:
        print(f"\n未匹配的标准（前20条）:")
        for s in unmatched[:20]:
            print(f"  {s['id']} | {s['name'][:50]}")

    # 统计覆盖情况
    with_data = len([key for key in classified.keys()])
    print(f"\n覆盖了 {with_data} 个不同的 子品类/生命周期/来源 组合")

if __name__ == "__main__":
    main()
