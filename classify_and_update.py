# ============================================================
# 1. 加载所有爬取数据
# ============================================================
def load_all_standards():
    all_stds = []
    seen_ids = set()
    for f in sorted(glob.glob(os.path.join(CRAWL_DIR, "*.json"))):
        try:
            data = json.load(open(f, encoding='utf-8'))
            for item in data:
                std_id = item.get('id', '').strip()
                if std_id and std_id not in seen_ids:
                    seen_ids.add(std_id)
                    all_stds.append({
                        'id': std_id,
                        'name': item.get('name', '').strip(),
                        'url': item.get('url', '').strip()
                    })
        except Exception as e:
            print(f"读取 {f} 失败: {e}")
    print(f"总计加载 {len(all_stds)} 条唯一标准")
    return all_stds

# ============================================================
# 2. 判断标准来源
# ============================================================
def get_source(std_id):
    if std_id.startswith('Q/GDW') or std_id.startswith('Q/CSG'):
        return '企业标准'
    elif std_id.startswith('GB') or std_id.startswith('GBT'):
        return '国家标准'
    else:
        return '行业标准'

