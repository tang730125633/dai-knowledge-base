# ========== 标准类型 → 目录名 ==========
STD_TYPE_FOLDER = {
    '国家标准': '国家标准',
    '行业标准': '行业标准',
    '企业标准': '企业标准',
    '地方标准': '地方标准',   # 🆕 新增（之前仓库没有）
    '未定': None,  # 不入库
}


def resolve_target_dir(keyword: str, category: str, phase: str, std_type: str) -> tuple:
    """
    返回 (大类目录, 子分类目录, 环节目录, 类型目录) 或 None

    输入来自 Excel 列：
    - keyword: samr 原始关键词 (如"抽水蓄能")
    - category: 自动推断的大类 (如"发电")
    - phase: 工程环节 (如"设计")
    - std_type: 类型 (如"行业标准")
    """
    # 1. 先用 keyword 精确匹配
    if keyword and keyword in KEYWORD_MAPPING:
        cat_dir, sub_dir = KEYWORD_MAPPING[keyword]
    elif category:
        # 2. 根据大类 + 模糊匹配找子分类
        cat_map = {'发电':'1、发电','输电':'2、输电','变电':'3、变电',
                   '配电':'4、配电','用电':'5、用电','电力交易':'5、用电'}
        cat_dir = cat_map.get(category)
        if not cat_dir:
            return None
        # 在该大类的已有子分类里找部分匹配
        sub_dir = None
        existing = EXISTING_SUBCATEGORIES.get(cat_dir, [])
        for sub in existing:
            if keyword and (keyword in sub or sub in keyword):
                sub_dir = sub
                break
        if not sub_dir:
            # 还是找不到 → 用一个通用子分类（需要新建）
            if cat_dir == '3、变电':
                sub_dir = '变电站'  # 笼统的变电内容
            elif cat_dir == '5、用电':
                sub_dir = '用电'
            elif cat_dir == '4、配电':
                sub_dir = '配电'
            else:
                sub_dir = keyword or '其他'
    else:
        return None

    # 3. 工程环节
    phase_dir = PHASE_MAPPING.get(phase, '1、勘测设计')

    # 4. 标准类型
    type_dir = STD_TYPE_FOLDER.get(std_type)
    if not type_dir:
        return None

    return (cat_dir, sub_dir, phase_dir, type_dir)


